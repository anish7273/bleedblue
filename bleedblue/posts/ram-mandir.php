        First of all very happy Independence Day to all my fellow Countrymen.<br>
<img style="border-radius:8px;margin:40px 10px;" class="d-block img-fluid  ml-auto mr-auto" src="../post/india.jpg" alt="india"><p class="img-caption caption text-center"
 style="margin-top:-40px;font-size:0.8em;border-bottom:50px">Happy Independence Day</p>
 

<br><br>We are back once again although the gap was a long one this time, all thanks to our NIT administration for the online examinations. Also it was the unfortunate accident at the Calicut International Airport, that disturbed the team mentally and physically too.
<br><br>But as they say, 'Der aaye par durust aaye'(For my friends who didn't got that it meant, Better late than never'). And for this time we've got the most difficult nerve to pick up as of now.
<br><br>As we all witnessed the recent confrontation in the Silicon city, Bengaluru, between a particular sector of society. It filled my heart as a citizen of India to see the people disturbing the communal harmony of the nation.
<br><br>But before we begin, I want to ask a question to all of you out there, " Is this right to keep the Religion before the Nation?"
</p>

<p>Whatever happened, it shouldn't have been happened on the first cause but whatever is done can't be undone and let's see what actually happened at the site.
<br><br>A man by the name of P.Naveen uploads a post on Facebook that a particular section of the society finds objectionable. Now ideally should have been done- The people finding the post objectionable should have asked him to take down the post and and ask for apology (which he ultimately did) and if he refuses to do so then he should be approached legally.
<br><br>But unfortunately what happened- A mob gathers in front of Naveen's Uncle's Residence who eventually happens to be the MLA of the region and started rioting and demolishing of Public Property.

But as they say a mob has no mind. This incident reminds me of one more similar incident where a historical building faced he heat of the mob. This was unambiguously the most shameful incident in the history of modern India.
<br><br>This was the day of December 06, 1992. Well before you decide to judge me and try to ascertain my religious beliefs. I urge you to know the matter in deep than make a decision about me in your psyche.
</p>


<h3><u>The Ram Mandir/Babri Masjid Land Dispute</u></h3>
This had been a heated topic in the Nation's news for more than a century. These disputes started as small conflicts between the two religious communities dating as long as 1850's. Lets witness the saga as it happened in the form of a timeline as the events unfolded from 1900's till date.
 
<div class="window">
<h3><b>Timeline</b></h3>

Since the early 19th century, session courts in Ayodhya have received a number of cases regarding the disputed site.
<br><br>Allahabad High-Court decided to club all the concerned cases together and ordered the status quo to be maintained. In a more simple language to be understood by peeps like us, HC insisted to let the situation remain as it had ever been.


<h3><b>November, 1989</b></h3>
A group of 14 people led by Vishwa Hindu Parishad(VHP) & Bharatiya Janata Party(BJP), performed the Shilanyas or the foundation laying ceremony for the Temple. However, the then Central government opposed the move concerning the official order & also the security reasons. VHP leadership assured the government that the ceremony would be done at a distant site & the peace of hour would be maintained. 
<br><br>Here, the government's approval itself raises an array of questions. When the HC had ordered the status quo to prevail, how could the gov. allow the ceremony to be held?
<br><br>Was this move by the government greatly influenced by some internal political matter? Well, the 1989 General Elections were just round the corner. Was the Rajiv Gandhi-led gov. keen to set a mandate to the common masses? Was the approval meant to be a trump card for the Indian National Congress(INC) to thrive in the forthcoming elections? Well, I would prefer the readers to take your own discretion in consideration & let it be decided by yourselves.

<br><br>The Shilanyas didn't fail to spread the anti-Muslim propaganda in the political circle which rapidly turned into violence claiming lives of more than a 1000 people all across the nation. 
<br><br>Eventually, the INC lost the 1989 General Elections, leading a way for Mr. VP Singh to rise to the prominence with the help of BJP in coalition. BJP on the other hand, managed to succeed a significant total of 85 LokSabha seats this time, compared to 2 in '84. 
<br><br>This shows how a religious affair turned into an undesired political manifesto.

<h3><b>September-October, 1990</b></h3>
The BJP supremo, Shri Lal Krishna Advani Ji had always been determined. He took the controversial issue to an upgraded level as he, along with the Karsevaks(a group of people who were core supporters for the Ram Mandir to be constructed, led to a rally called "The Rath Yatra" across the country to gain support for the cause.
<br><br>This Yatra, however, turned out to be a trail of blood as the march attracted a number of unfortunate communal riots in the country. The riots accounted for the loss of as many as above 100 lives.


<h3><b>October 30th, 1990</b></h3>
The Rath Yatra reached the city of Ayodhya & the Karsevaks tried to bring down the monument standing on the disputed land. However, the then Uttar Pradesh gov. ensured such activities from happening by setting up an aggressive fielding of the security forces. This led to another bloody encounter which claimed the lives of a number of Karsevaks.


<h3><b>1991..</b></h3>
BJP gained the power after an emphatic triumph in the Vidhan Sabha Elections. The erstwhile gov acquired the 2.77 acres of land around the disputed site for the name of tourism & allegedly handed it over to Janmabhumi Nyas.
<br><br>Meanwhile, the HC ordered to restrict any permanent construction around the site.

<h3><b>July, 1992</b></h3>
While the State government vehemently supported the VHP & the Karsevaks, the Centre showed reluctance. This added up more tension over the issue. 
<br><br>Later in July, the Karsevaks gathered once again, to start the construction of a platform around  the site.  

<h3><b>October 1992</b></h3>
VHP organises its Dharam Sansad or the religous Committee in Delhi Concluding to assemble once again on 6th of December at the site to observe the day as Karseva Divas.
<br><br>This development bumbled the central government as the Home Minister urged the PM to dismiss the state government and force governor rule to avoid he gathering. Maybe he had realised the upcoming future.
<br><br>But the two bladed state government on one side assured the centre to save its ass from execution and on the other hand the CM of the state publicly Announces that the police and other armed forces are instructed not to fire on the Karsevaks.
<br><br>It clearly seems something fishy is going on. Daal mai kuch kaala nahi puri daal hi kaali hai.

<h3><b>December 06,1992-Doom's Day</b></h3>
The senior BJP/VHP contingent marked their presence with warm speeches. But as they say a Mob has no mind, their speeches turned to be the fuel for fire and the historic monument faced its fire. With whatever the mob found rails, rods, stones the Babri faced the blow.
<img style="border-radius:8px;margin:40px 10px;" class="d-block img-fluid  ml-auto mr-auto" src="../post/babri.jpeg" alt="doom"><p class="img-caption caption text-center"
 style="margin-top:-40px;font-size:0.8em;border-bottom:50px">The Mob's Anger</p>
<br><br>The Karsevaks demolished the building within hours and established an interim Rudimentary Ram Lalla idol at the site.
<br><br>Well this wasn't a small affair a historical monument has been demolished the accused would have to pay for it.
<br><br>The first strike came the very evening with the resignation of CM Kalyan Singh.
<br><br>But it raises a few more questions in my psyche. When the possibility was ascertain why wasn't the armed forces there where was the police?
<br><br>Well to our surprise it was right there but only to be outnumbered by the mob of nearly Two Hundred Thousand people and what could have they done with the orders not to fire at the mob.
<br><br>The situation is quite similar to as if you're told to write an exam but without a pen( no no no, no the online one but the actual pen and paper based). Well what can you do there other than staring the question paper.        

<h3><b>The Aftermath</b></h3>
The barbarians act by the mob sparked the communal Hindu-Muslim riots not just in the nation but the whole Indian subcontinent.
<br><ul>
<li>Bombay faced the worst heat with more than 1000 people losing their lives.
</li><li>The bloody riots ate up more than 2000 people across the nation and acted as the cause of many upcoming communal attacks too.
</li><li>Bangladesh also witnessed the anti-Hindu sentiment with the demolition of temples and Civil Buildings.
</li><li>Consulate General of India in Karachi was also disturbed.
</li><li>And the most infamous 1993 Bombay serial blasts.
</li></ul>

<h3><b>Liberhann Commission</b></h3>
The culprits of the act can't just go away. Just a resignation won't justify lives of many innocents. Consequently the Liberhann Commission was formed under justice Liberhann to find the wrongdoers. After 17 years the commission gave its report in 2009 withstanding 68 people accused with the major culprit being the then CM Kalyan Singh,.
<br><ul><li> The report accused RSS/VHP as the chief architects of the demolition.
</li><li> Whereas the BJP and RSS leadership termed the Liberhann Commission report as politically motivated.  
</li></ul>
<br><br>But was the report politically motivated?
<br><br>While its definitely sure that this affair is more sort of a political affair rather than a religious affair, but I doubt about the political motive behind the report of Judicial Commission.
<br><br>Now once again moving on to Allahabad High Court.

<h3><b>ASI Excavations</b></h3>
On directions of the HC, Archaeological Survey of India was told to find out proofs of any temple under the structure on which ASI told that-
<br><br>ASI found significant artefacts including pillars showing Hindu Motifs which point out to a large Hindu-complex pre-dating the mosque from around 10th century
<br><br>But one noticeable fact is that ASI report never out rightly told that the complex was the Ram Mandir, Once again leaving clouds of dub behind it.

<h3><b>September 30, 2010</b></h3>
Allahabad High Court accepted that the disputed site was the birthplace of Lord Rama. But the court didn't ignored the Babri masjid. As a result, the court announced a three sided verdict.
<br><br>The disputed Land to be divide into three parts.
  <br><ul><li>Ram Lalla (represented by the Janmabhumi Nyas) was given the central shrine where the rudimentary temple was established
  <li></li>Nirmohi Akhara was given the Sita Rasoi and Ram Chabutra which were the peripheral structures of the site.
  <li></li>Sunni Waqf Board was awarded the rest of the land.
	</li></ul>
<br><br>As obvious, None of the parties seemed satisfied with the verdict As a result the case again moved to the supreme court.


<h3><b>November 09,2019</b></h3>
<br><br>Supreme Court of India gave its verdict.
<br><br>The disputed site went to Lord Rama (well quite astonishingly but our constitution gives these rights to lords too) and the Janmabhumi Nyas was given the rights of the land for the maintaining and management rights of the site.
<br><br>Meanwhile SC didn't disappointed the other community too. SC ordered the government to allot 5 acres of prominent land to Sunni Waqf Board in Ayodhya for the Mosque Complex.  



<br><br><br><br>That's the whole timeline of events as it rolled out since the early 19th century till date.
</div>
<br><br>Well I'm not an expert to criticize the government and quite obviously as a citizen of India I don't have any rights to question the decision of the Honorary Supreme court of India.
<br><br>But wait a minute, does this means that I won't say anything on the matter?
<br><br>No it's not like that , I have an opinion but before that I'll give a disclaimer to that these are my personal views and I apologise to anyone who takes this as offensive. 
<br><br>See the mosque was an  historic building that stood there for atleast 500 years. And even if it stood on the site of a temple the past cannot be undone. If you go like this how far will you go to rectify what has been done in the history.
<br><br>And the matter of fact is that this disputed land has become much more a political affair rather than a religious one.
<br><br>Now as the situation stands whether it be Ayodhya, Delhi or Bengaluru its just one political affair that sparks a fire that aims to burn the auspicious string of  harmony between the sons of the motherland. 
<br><br>Now tell me one thing, Do we really need these structures to serve the almighty? Do we need more Mandirs? Do we need more Mosques?
<br><br> Definitely not a the cost of many innocent lives. If you want to serve the almighty, try to see him with a pure heart. The almighty is everywhere.
 
<br><br>Just tell me one thing. When the Corona broke out, Didn't you studied from your home miles away from your colleges? Didn't your family men worked from home?
<br><br>They did. Didn't they achieved their work targets? Didn't you excelled your semester exams? You did. When we can study without going to school, when we can work without going to office, why can't we serve the almighty, without these temporary structures?

<br><br>On this propitious occasion of National Independence Day, I request you to pledge along with me-
<br><br><i>"In the upcoming future, I'll never pay heed to any hatred topic that infringes my nation's integrity and Communal Harmony."</i>          

	
