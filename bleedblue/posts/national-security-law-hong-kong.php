<p>Not long before, until 1997 Hong Kong used to be a  British territory. After the loss of Chinese empire to Great Britain in the opium war,  Hong Kong went into British hands for a lease of 99 years which ended in 1997. After that a deal was signed between Britain and China to give Hong Kong back to China for the next 50 years on the condition that Hong Kong would be a special autonomous region, it will follow the ‚ÄòOne country, Two system' principle. For discernment, consider it like the article 370 and 35A of Indian Constitution. Under this system, Hong Kong has autonomy over its internal matters whereas China will handle it‚Äôs defence and foreign policy. <br>
