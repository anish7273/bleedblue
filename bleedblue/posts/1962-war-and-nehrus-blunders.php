        Have you ever seen or heard of a Bank Manager burning down the currency notes in  Bank’s Possession? Or The Manager drowning sacks full of Coins in Ponds?<br>
No! I’m neither cooking up a story nor blowing air to a baseless rumour. It’s the story of a small town situated on the northern bank of the Brahmaputra r in Assam.<br>
In a single night, all the senior administrative figures DC, SP had already fled through a small airport on the outskirts of the city, leaving behind none to guide the common. It’s hard to imagine the chaos that descended on Tezpur that day. Thousands of bullocks carts and Vehicles crowding the steamer boats to flee to the other side of the river.<br>
But, Why the hell are these people leaving?  Is there any calamity coming? <br>
Yes there is, It’s the onset of 1962 Indo-China War. When Pt. Nehru addressed the Nation on All India Radio following the fall on Bondila to China, leaving the fate of people of Assam on Chinese forces, the action of people of Tezpur is rightly justified.<br><br>
<h3><u>The Bitter truth of ‘62</u></h3>
The 1962 war is that piece of Indian Modern history that never fails to hurt the sentiments of our People. Even while preparing this article, My spirits refused a number of times to me. But the question is, why it hits so hard?<br>
Probably because we never fought this war to win, but to survive.  This was the stand of the  country's top political and bureaucratic heads of the time. Have a glance at this excerpt from Indian Parliament what Nehru excused after the loss of Aksai Chin to China. Nehru told, “ Not a single blade of grass grow there.”<br>
But fortunately we had a few people who really cared for the country, on this very statement of Nehru, Shri Mahavir  Tyagi, a Senior Congressman stood up pointing towards his bald head and proclaimed, “ Nehru Ji even nothing grows  here should I offer this as well to Chinese.”<br>
This determines up to what extent Nehru has failed the Nation. His failure raised a few questions that are still awaiting answers.<br><ul><li>Why isn’t the govt. declassifying the Henderson-Brooks Report on the ’62 war?</li><li>Why was General Kaul was made the chief of army staff? Was the military in hands of political Babus ?</li><li>
Why wasn’t the Air force used even in such tense situations? </li><li>
Whereabouts is the India’s seat in UN security Council?</li></ul><br>
While many of these questions will remain unanswered even now and will remain as it is for longtime to come. We’ll figure out a few here down below.<br><br>
<h3><u>Was the Army leadership In wrong hands?</u></h3>
While the appointment of General Kaul as Chief of Army Staff is still an unanswered question. This points out some serious questions on Nehru’s leadership. We won’t be discussing that here as it infringes the dignity of Indian Army, that’ll never happen on BleedBlue’s.<br>
<a href="https://www.indiatoday.in/india/story/the-guilty-men-of-1962-india-china-war-jawahar-lal-nehru-krishna-menon-185333-2014-03-18">  Maybe, you’ll find a possible explanation here.</a><br>
We’ll figure out how miserably the Nehru led Congress govt. failed the Nation, Let’s take an excerpt right from the very beginning  i.e. since 1947. Where the roots of Independent Dominion of India or as better know now The Republic of India starts.<br>
Following Independence, Nehru called an party meeting consisting of the then Defence Minister and  Senior Party Officers. The agenda at hand was to appoint the New Chief of Army Staff. At which Nehru proposed, “I think we should appoint a British Officer as a General of Indian Army, as we don’t have enough experience to lead an Army.” Well the most of morons in the Congress seemed agreeing to the proposal, but fortunately we had a few sensible delegates in the meeting. As natural, “Lt. General Nathu Singh countered, “You see Sir, We don’t have enough experience to lead a nation too, so shouldn’t we appoint a Britisher as Prime Minister of India?<br>
But Unfortunately,  Lt. General  failed to open Nehru’s eyes who was too engrossed in politics to see the Nation’s Interests. As a result Sir Robert McGregor MacDonald Lockhart was made the first Chief of Army Staff even again after Sir Robert ,CoAS was appointed as an English Man, Sir Francis Robert Roy Butcher.<br>
Many blames whom as incompetent leaders and one of the reasons why a part of Kashmir still lies on the other side of the Border.<br><br>
<h3><u>Where was the Air force in ‘62</u></h3>
As quoted by former Air Vice-Marshall A.K. Tewary, the results of the war could have been otherwise, Had India used its Air force?<br>
Tewary Sir held the then political and bureaucratic leadership responsible for it. Nehru’s govt. sought for the help of US Air force but didn’t even consulted IAF.<br>
Saw, How Chacha Ji displayed epitome of leadership by ditching our own Indian Air force for the foreign Aid. What an incredible leader Chacha Ji was?<br>
At the war, IAF could have easily outdone the Chinese PLAAF with nearly 22 combat ready squadrons with over 500 aircrafts with the likes of Hunter-Mk56, Folland Gnats, French Mystere, Toofani, Canberra Bomber and the Vampire ground attack Aircraft, Had Nehru not restricted  the Air force?<br>
Chacha Ji would have realised soon how costly and catastrophic the omission was.<br><br>
<h3><u>Nehru’s China-UN Policy</u></h3>
Well do you know, Why half of Kashmir is still in Pakistani hands?<br>
It’s because our dear, Chacha Ji requested UN to mandate ceasefire on the border going against the likes of  Ironman Patel and Veteran Field Marshal Cariappa. Ironically India is still not a permanent member of the same UN that costed us our territory, Don’t you want to know the reason behind it?<br>
Kudos to China, China is like that snake in the game of Snake and Ladders that bites just before reaching home. China is the only member of UN security council, blocking India’s entry to the institution. Well all the credit doesn’t go to China only Nehru Ji too played a significant role to shape the situations as they are today.<br>
Many people don’t know about this blunder of  The Renowned Barrister from Cambridge. Pundit Ji refused  the offer of permanent seat on UN security council not just once but twice. In 1950’s, United States offered India the UN seat owing to India’s significant position in the Indian subcontinent but Pundit Ji refused deliberately. Then again in 1955, Soviet Premier Nikolai Bulganin offered the UN seat, Pundit Ji backed again. Well at this point of time many of  you might be thinking, what the hell was going around this man that he refused both the World Superpowers that too for UN seat, that India is still struggling to reach at even now in 2020. <br>
Well it seems Nehru was too influenced by his good for nothing, “Hindi Chini Bhai Bhai” policy. Nehru refused UN seat saying not at the cost of China. Actually Republic of China(ROC), not to be confused by People’s Republic of China(PRC) which is controlled by the Communist Chinese Party, had a seat in UN but as PRC defeated ROC and forced it to retreat from Mainland China to Taiwan. This ended the international recognition of ROC and its UN seat too, this was the seat being offered to Nehru again and again.<br>
But Nehru had something else going on in his mind, for which the whole nation paid back in the form of Arunachal Pradesh, Assam, Aksai Chin and are still paying in the form of  Ladakh and Galwan valley.<br><br>
<h3><u>Conclusion</u></h3>
Nehru’s Policies have failed the Nation again and again be it the <br><ul><li>
Saying no to King of Baluchistan to merge in the Indian Union, or be it
Leaving half of the Kashmir to the other side of Border, or be it</li><li>
Rejecting Nepal’s wish to join the Nation as one of it’s state, or be it</li><li>
Gifting the strategically important territories of Coco island in straight of Malacca to Myanmar, or be it</li><li>
The  Hindi Chini Bhai Bhai sentiment.</li></ul><br>
