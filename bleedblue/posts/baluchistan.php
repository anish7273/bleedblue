<h2><u>The One Where it all started</u></h2>

<p>The first A - Allah- in Pakistani politics has always been used by Islamabad to colonize Balochistan. Baloch nationalist leaders, who are secular, believe that the religion shouldn't be intertwined with politics. However the second A- Army - has time and again threatened the Baloch, at the gunpoint & gunship helicopters that their salvation lies in becoming Pakistani & good Muslims.<br>

It all started in 1947 when the British India was being partitioned. 535 princely states were given the will to either declare independence or accede to one of the two pre existing domains. The State of Kalat, one of the princely states of Balochistan, was granted independence on Aug 11th, 1947. The fore-founder of Pakistan, Mr. M.A. Jinnah accepted the sovereign status of Balochistan only until the British left. On March 27th, 1948, the Khan of Kalat, Ahmad Yar Khan was made to sign the accession under duress by Jinnah.<br>

Resented by the unconstitutional annexation, Khan's younger brother, Prince Abdul Karim took refuge in Afghanistan in order to wage an unconventional attack against Pakistan. The lone wolf fought for the pack only to be assassinated by the Pak Army in 1950. <br>

This is how Pakistan inflicted the first bloody wound into the heart of Balochistan which shows no sign of healing even today.<br><br></p>


<h2 class="mb-0"><u>Dilemma Of Balochistan </u></h2><br><h4 class="mt-0">Plethora of mass killings & disappearances</h4>
 
<p>'Human rights' is probably the most advocated term among the intellectuals of the modern world.<br>
The Baloch have been subjected to a plethora of mass killings, disappearances & even religious persecutions of Baloch minorities.<br>

An instance of Pakistan's barbarism over Baloch people was the counterinsurgency operations of 1959. The then Pak premier, Chaudhary Mohammad Ali, implemented the framework of 'One Unit' program on Oct 14th, 1955. Baloch leader, Navam Nauroz Khan took up arms in resistance to the unlawful program which was meant to end the provincial status of Balochistan. Pakistan went all out against the innocents. Frontiers corps committed unconscionable & the most brutal act of state terrorism in Balochistan, causing the Nawab to surrender. All of his sons & family persons were tortured & then hanged to death. Nawab was charged with sedition & was sentenced to life imprisonment.<br>
 
<h5>Fast forward to 1973. </h5><br>

Pakistan had suffered the worst possible defeat by the Indian Armed Forces in the 1971 Indo-Pak war. Forcing East Pakistan to lose its existence. General Tikka Khan, out of exasperation, deployed Pak Air Force in Balochistan to curb the rising insurgency. PAF bombarded the very heart of Balochistan with F-86s & Mirage. Even Iranian Shah offered Cobra Helicopters to carry out the operation. Pak Gen went remorseless & the <thd></thd> holocaust killed hundreds & thousands of Baloch.<br>

Since then, many Baloch leaders have been quashed or abducted by the Pak forces.<br>

"They are treating the Baloch the very same way, ISIS treats the Yazidis." , quoted by Kachkal Ali, the former Fisheries Minister of Balochistan.<br><br></p>


<h2><u>Strategical Significance of Balochistan</u></h2>

<p>Balochistan is the largest province of Pakistan, constituting almost 44% of Pakistan's land mass. It sits on probably the most critical junction of the world as it provides the connecting passage between the Middle East, the Central Asia & the Indian subcontinent. It lies at the mouth of the Hormuz Strait thereby providing the shortest sea route to Central Asia.<br>

Balochistan has always been rich in exhaustible & renewable sources. It is the second major supplier of Natural Gas in Pakistan. Minerals like Copper, Gold, Coal & Uranium have been found in abundance here.<br>

2/3rd of Pak's coastline lies in Balochistan. You guys must have heard about Gwadar Port. Haven't you?? <br>
Yeah, the one Pakistan ceded to ever ambitious Chinese project, the China Pakistan Economic Corridor (CPEC), worth a whopping amount of 60 Billion US Dollars, has its termination point at Gwadar port. This port is referred to as a Chinese trump card in case of any future hostilities against India. <br><br></p>


<h2><u>Numbers that speak for themselves</u></h2>

<p>Stats taken from the book "The Balochistan Conundrum" by Tilak Deavsher, once being the Cabinet Secretariat, give us a brief of the abysmal persecutions of the Baloch on their own land.<br>

Prior to the annexation, Balochistan used to enjoy a share of 4.9% of Pakistan's total territorial GDP which later depreciated miserably to 3.7% in today's world. Balochistan has also the highest poverty rate & the lowest literacy rate among all other provinces to its name.<br>

Balochistan has the highest Infant & Maternal mortality rate among all the Pakistani provinces.<br>
Don't forget that PoK is often considered to be underdeveloped & unsuitable for sustainable living, but there hasn't been much talk to share the Baloch plights globally. The third A in Pakistani politics, ie. America has turned out to be absolutely thwarted for Baloch. One of the best US diplomats-scholars Dr. Henry Kissinger famously said, " I wouldn't recognize the Balochistan problem if it hit me in the face". <br>

Gwadar lacks portable water. People there die of thrust but to no avail to Pak's governments. China will exploit their resources in coming years & the Baloch will be doomed ultimately.<br>

Pakistan feeds on Baloch resources, it thrives on their coal, natural gas & other minerals as well. Infact, Pakistan owes its existence to Balochistan & yet no reimbursement has been provided over the years to them.<br><br></p>


<h2><u>Conclusion</u></h2>

<p>Pakistan has inflicted numerous wounds into Balochistan's very heart & these wounds show no signs of healing even today. The Baloch Representatives are bound to take asylum in foreign countries.<br>
My question to you guys is, "Are there any methods or chances to counter the Baloch plights?".<br>
I think their certainly is.<br>

 Pakistan has been declared a failed state. Economy is on the verge of shutdown. An internal implosion or a State Wreckage is an inevitable situation. 
Balochistan needs an economically stable population & moreover a revolutionary & modernized representation. A tribal Balochistan needs to be replaced by a Unified Balochistan.<br>

This may be a Black Swan event but who knows what the future unfolds. Do you?<br>

I hope you guys have enjoyed this anecdote. Thanks a lot for staying till the end with me. We will come back with another story to tell. Until then, have a good day & a good life.<br><br></p>

