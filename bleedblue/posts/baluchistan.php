However, these 'A's cease to exist when we ponder over our neighbor as well as nemesis, Pakistan. In Pakistani politics, it's true that the three 'A's "<b>Allah, Army & America</b>" have always mattered the most. The 3 'A's might have turned out to be a boon for the aakas of Pakistan but the Balochs & the Balochistan have always been on the wrong side of these 'A's. <br>
Let's start our story from a ghazal by revolutionary Pakistani poet, Habib Jalib which takes a dig over Pakistan's atrocities over Balochistan.<br><br><br>
