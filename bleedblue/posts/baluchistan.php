        <p>Namaste Blue_bloods! I hope you guys are doing good with your life amidst the pandemic threat.<br>

Have you guys ever wondered why the progenies of a great nation, Pakistan & India now stand wide apart from each other in terms of all positive parameters? What has caused this wide abyss of differences among the two? Both shared almost similar resources, labour, infrastructure & even monetary base post independence. What eventually led to a nation being the world's biggest democracy & the other being a banana state-
Terroristan?<br>

Let's talk about the 3 'A's which are considered vital for a smooth run of a governing body democratically.<br><br>

<b><u style="font-size:2em">A</u></b>bility to govern the masses<br>

<b><u style="font-size:2em">A</u></b>ccountability for its actions & ideologies<br>
