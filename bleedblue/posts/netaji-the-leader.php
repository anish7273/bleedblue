                <h2>Neta Ji- The man who knew the cost of freedom<a href="../docx/netaji.docx" download> <img class="pull-right" src="../post/dwnlod.png" height="50"></a></h2>
                  2020,Aug 15 | 02:00PM<br>Peeyush Mishra in collabration with Aditya Shukla
                </div>
		<div class="card-body">
        <b>“Tum Mujhe Khoon Do, Mai Tumhe Aazadi Doonga”</b><br><br>

<br><br>These are not some petty words written with the motives to create an adrenaline rush, but these were the words that energized an entire nation. With the call of “Give me your blood and I will give you freedom” one man challenged the might of an empire and inspired a nation to join hands to free itself from the shackles of Imperialism. This man was none other than Netaji Subhash Chandra Bose, to whom we pay our richest tributes on his death anniversary today.
<br><br>When we remember the glorious history of India’s freedom struggle, the name of Netaji Bose is marked in golden letters. His name is edged in the hearts and minds of every Indian. The nation remembers Netaji as a brave soldier who devoted his entire life to his country so that his fellow brothers and sisters breathe the air of freedom, liberty and dignity.

<br><br><b>What do we know about Netaji’s childhood and early life?</b>
<br>Netaji was born on 23 January 1897 in Cuttack (Odisha) to Janakinath Bose and Prabhavati Devi. Among the eight brothers and six sisters, Subhash was the ninth child. His father, Janakinath Bose was an affluent and successful lawyer in Cuttack. He had received the title of “Raj Bahadur”. He later went on to become a member of Bengal Legislative Council.
<br><br>Talking about academics, Netaji was a brilliant student. He completed his B.A. in Philosophy from the Presidency College in Calcutta (now Kolkata). He was deeply motivated and influenced by Swami Vivekananda’s teachings. He was known for his patriotic zeal since his student days. In an infamous incident where Bose beat up his professor (E.F Otten) for passing a racial remark, brought him infamy as a rebel-Indian in the eyes of the government. Talking about his father’s expectations, Jankinath Bose wanted Netaji to become a civil servant and therefore, sent him to England to appear for the Indian Civil Service Examination. Acing the test, Bose was placed fourth with highest marks in English. But this achievement didn’t hold much importance to Netaji as his urge for participating in the freedom movement was intense. This led him to resign in April 1921 from the coveted Indian Civil Service and come back to India. In December 1921, Bose was arrested and imprisoned for organizing a boycott of the celebrations to mark the Prince of Wales' visit to India.

<br><br>During his stay in Berlin, he met and fell in love with Emily Schenkl, who was of Austrian origin. Netaji and Emily were married in 1937 in a secret Hindu ceremony and Emily gave birth to a daughter Anita in 1942. Shortly after the birth of their daughter, Bose left Germany in 1943 to come back to India.

<br><br><b>Rising to the prominence & Disagreements with the Indian National Congress(INC)</b>

<br><br>Cut to short, Netaji was swashbuckler. Bose had been a leader of the younger, radical, wing of the Indian National Congress in the late 1920s and 1930s, rising to become Congress President in 1938 and 1939. In 1939, He was elected the President of the INC by defeating Pattabhi Sitaramayya , who had been supported by Gandhi himself. "This was the first time in the past two decades that Gandhi's authority had been challenged withing the Indian National Congress", Bose said in his lecture lasting for nearly an hour. However, he was ousted from Congress leadership positions in 1939 following differences with Mahatma Gandhi and the Congress high command. He was subsequently placed under house arrest by the British before escaping from India in 1940.
<br><br>INC, the pillar of the Indian nationalism, praised Bose's patriotism but distanced itself from his tactics and ideology, especially his collaboration with fascism. The British Raj, though never seriously threatened by the INA, charged 300 INA officers with treason in the INA trials, but eventually backtracked in the face both of popular sentiment and of its own end.

<br><br><b>Catastasis of a revolution,the AZAD HIND FAUJ</b>
<br><br><i>"LIFE LOSES HALF ITS INTEREST IF THERE'S NO STRUGGLE, IF THERE ARE NO RISKS TO BE TAKEN".</i>
<br><br>Bose fooled the British remarkably. He eacaped the house arrest in disguise &  arrived in Germany in April 1941, where the leadership offered unexpected, if sometimes ambivalent, sympathy for the cause of India's independence, contrasting starkly with its attitudes towards other colonised peoples and ethnic communities. In November 1941, with German funds, a Free India Centre was set up in Berlin, and soon a Free India Radio, on which Bose used to broadcast nightly. A 3,000-strong Free India Legion, comprising Indians captured by Erwin Rommel's Afrika Korps, was also formed to aid in a possible future German land invasion of India. By spring 1942, in light of Japanese victories in southeast Asia and changing German priorities, a German invasion of India became untenable, and Bose became keen to move to southeast Asia. Adolf Hitler, during his only meeting with Bose in late May 1942, suggested the same, and offered to arrange for a submarine. Identifying strongly with the Axis powers, and no longer apologetically, Bose boarded a German submarine in February 1943. Off Madagascar, he was transferred to a Japanese submarine from which he disembarked in Japanese-held Sumatra in May 1943.

<br><br>With Japanese support, Bose revamped the AZAD HIND FAUJ or INDIAN NATIONAL ARMY (INA), then composed of Indian soldiers of the British Indian army who had been captured in the Battle of Singapore. To these, after Bose's arrival, were added enlisting Indian civilians in Malaya and Singapore. The Japanese had come to support a number of puppet and provisional governments in the captured regions, such as those in Burma, the Philippines and Manchukuo. Before long the Provisional Government of Free India, presided by Bose, was formed in the Japanese-occupied Andaman and Nicobar Islands. Bose had great drive and charisma, creating popular Indian slogans, such as "Jai Hind," and the INA under Bose was a model of diversity by region, ethnicity, religion, and even gender. In late 1944 and early 1945, the British Indian Army first halted and then devastatingly reversed the Japanese attack on India. Almost half the Japanese forces and fully half the participating INA contingent were killed. The INA was driven down the Malay Peninsula and surrendered with the recapture of Singapore. Bose had earlier chosen not to surrender with his forces or with the Japanese, but rather to escape to Manchuria with a view to seeking a future in the Soviet Union which he believed to be turning anti-Briitish.


<br><br><b>I’ve heard that Netaji is still alive. Is he dead or alive?</b>
<br><br>Over the decades, this has been a very controversial topic. The speculation just refuses to die down. It is said that soon after the unfortunate retreat of INA, Netaji mysteriously disappeared. It is believed that he went back to Singapore and met Field Marshal Hisaichi Terauchi, head of military operations in South East Asia who arranged for a flight to Tokyo for Netaji. He boarded a Mitsubishi Ki-21 heavy bomber from Saigon Airport on August 17, 1945. Unfortunately, the bomber crashed shortly after take-off after a night halt in Taiwan. Witnesses reported that Bose sustained intense third-degree burns in the process. He succumbed to his injuries on Aug 18, 1945. He was cremated on August 20 in Taihoku Crematorium and his ashes were laid to rest at the Renkji Temple of Nichiren Buddhism in Tokyo.
<br><br>Bose’s comrades who were stranded in Saigon waiting to be transported never saw his body. Nor did they see any photographs of his injuries. They refused to believe that their hero was dead and hoped that he evaded detection by the British-American forces. They believed wholeheartedly that it was just a matter of time that Netaji will gather up his army and conduct a march towards Delhi. Soon people began to report the sighting of the hero and even Gandhi expressed his scepticism about the death of Bose. Post-independence, people started to believe that Netaji had adopted an ascetic life and became a Sadhu. Other theories say that he spent his last days in Uttar Pradesh. The mysteries surrounding Bose’s death took upon mythic proportions and perhaps symbolized the hope of the nation.
<br><br>The government of India set up several committees to investigate the case. First, the Figgess Report in 1946 and then the Shah Nawaz Committee in 1956, concluded that Bose had indeed died in the crash in Taiwan.
<br><br>Later, the Khosla Commission (1970) concurred with the earlier reports, the reports of Justice Mukherjee Commission (2006) said, "Bose did not die in the plane crash and the ashes at Renkoji temple are not his". However, the findings were rejected by the Government of India.
<br><br>In 2016, following the declassification of a report handed over by the Japanese government to the Indian Embassy in Tokyo in 1956, titled "Investigation on the cause of death and other matters of the late Subhash Chandra Bose" confirmed the Indian National Hero’s death in Taiwan on August 18, 1945.

<br><br><b>Can you tell something about Netaji’s ideology?</b>
<br><br><i><b>"Freedom is not given, it is taken".</b></i>
<br><br>Netaji was one of the most effective catalysts who paved & smoothed the way of independence for India. His life was full of ecstasy & agony & yet he never drived off his virtues. Post 1939-era was crucial. British Raj wasn't the ever strongest then. The clutches were loosening. It was Netaji who mastered the art of conversion. It was he who the calamity to a chance. A chance of self-rule, a chance of Independence.
<br><br><i><b>"Cometh the Hour, cometh the Man"</b></i>

<br><br>Bose’s correspondences prove his faith in democracy in Independent India. Bose’s primary ideology was always the freedom of his motherland even if meant taking help from fascists like Mussolini or Hitler. 
<br><br>People often assume that since the ideology of Gandhi and Bose widely differed, they despised each other. It must be noted that Gandhi and Bose held each other in high regard as a valiant son of Mother India and a person, despite political differences. 


<br><br>Netaji Subhash Chandra Bose has left a deep impact on the psyche of his countrymen. His slogan, ‘Jai Hind’ is still used in reverence to the country. The International airport in Kolkata has been named Netaji Subhash Chandra Bose International Airport to commemorate the charismatic leader.  
<br><br>An article can’t articulate the legacy of perhaps the bravest son of this soil, but as a responsible citizen of India, we should remember that the freedom which we enjoy is the result of innumerable sacrifices and struggle of our forefathers. They are the real heroes. You are the true hero Netaji.

<br><br>We aren't sure that you hugged martyrdom this day in 1945. We don't know if you ever lived after. We never know whether you could sense the Independence in India. But we are sure that you kept the stride going. You were the one who the turned back the tide of time.
 & We, as an extended family of BlueBloodIndia, pay you the respectful tribute you deserve.
Thank You Netaji..
