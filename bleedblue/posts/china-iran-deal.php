<h2><u>China’s Interest in Iran </u></h2>
<p>It seems that this time China's eyeing much more than just Iran's territory. Iran has been blessed with huge energy reserves but the war-torn country doesn't have enough money to make these resources viable. It looks like this is what China is targeting at courtesy its multi-billion dollars investment. It's quite evident from the statement, of the promised $400 billion, <b>$280 billion alone is being spent on developing the oil and gas resources, </b>which in turn will give China access to cheap oil and gas. <br>
But China doesn't plan to stop even there, it's ultimate goal is the expansion of its esteemed<b> Belt and Road initiative</b> through Iran and Kazakh, in order to reduce its dependence on the strategically located, the Strait of Hormuz and the Strait of Malacca.<br>
