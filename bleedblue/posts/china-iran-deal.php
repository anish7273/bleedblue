Well, the <b> Belt and Road Initiative</b> is just one card of China 's brutal Modern day Colonialism. As of now<b>, China aims to bring more than 5000 PLA troops</b>  in Iran along with its investments, for its strategic needs. And I won't be surprised if this number rises exponentially in the near future and  we see a significant amount of PLA troops in Iranian territory . <br>
But why is Iran falling in this trap, don't they know of this trap?<br>
 As of now , I can only figure out one reason for it- <b>Mr. Donald Trump. </b></p><br>



<h2><u>US's Stand on Iran.</u></h2>
<p>To most of the people out there, it seems quite straight forward how the US has left no option for Iran. Have a look through the Indian perspective, not long ago, until the last year India was the largest consumer of Iranian oil. But as of now Indian import of Iranian oil stands to near zero. All thanks to the US sanctions on the bilateral trade with Iran. These sanctions were followed by <b>Trump's withdrawal from the Iran Nuclear Deal.</b> And while all this was going on, the US <b>assassinated Iran's top military leader- Qasem Soleimani</b> during a diplomatic mission to Iraq by a precise drone strike. <br>
Believe me or not, but the US government under<b> Donald Trump has only widened the gap between the Middle-East and the West, </b>which laid down a smooth ride for the Expansionist Communist Nation to jump in the gorge .<br></p><br>

<h2><u>Impact on INDIA </u></h2>
<p>With China shaking hands with Iran, India's diplomatic relation with Tehran has suffered a huge blow. As you all may know, India controls the Chabahar port in Iran and is also developing the Chabahar Railway Project to open the Central Asian Market to the Indian goods. But the recent developments suggest that Tehran has replied to New Delhi that they're developing the project on their own. But it's quite evident that it's  the Chinese regime that's supporting Iran to back out.<br>
The major issue is that the Chabahar project means a lot to us, we have already invested enough in it that we can't afford to lose it at this stage, but the Chinese entry into the region has only made the situation getting worse. At the same time, Iran's envoys to India, <b>Mr. Ali Chegeni's statement has created a turmoil in the political circles</b>. Here's the envoy's statement as he quoted, <b>“India had fought hard for its independence and should not have given it to unilateral sanctions from the US. '' </b><br>
While he was referring to India's shift of oil policy from Iran but this statement itself conveyed a critical message .<br>
Had he said anything irrelevant? Probably not. Infact, I strongly agree with the bureaucrat's statement.<br>
Had <b><u>Vajpayee Ji</u></b> been alive today, he too would have stated the same.</p><br>

