<p>Have your ears drums been shattered by this news of a whopping  400 Billion Dollar investment by  China in Iran?  I hope they aren't because mine have already blown out. Even the doctors are having a hectic schedule now-a-days, I'm already struggling to get their appointments. While I book an appointment for myself, why don't you have a deeper insight into the matter?<br>
It's been universally said & accepted,<b>" Your enemy's enemy is your potential friend.'' </b>Our dear neighbor  has taken it too seriously. China's growing hostility with the United States has brought it much closer to the oil-rich Iran. China has promised to invest <b>$400 Billion in Iran's economy in the span of next 25 year. </b>Well this amount is so emphatic that you and me can't even imagine this. To get to know a bit of the weight of this amount, consider the case of Pakistan. While Pakistan is drowning in Chinese debts all from head to toe, it owes China roughly $50 billion when only $50billion is enough to curb a nation. Just assume <b>what havocs can $400 billions do?</b> But what's  attracting China so much into this deal? Well there is more than one answer to this. Let's look at a few of these one-by one. <br>
Iran has been a war-struck Nation, that faces a hell lot of issues on the global fronts such as-<br><ul>    
<li>Iran has  been blacklisted by the UN's FATF body.</li>
    <li>Iran doesn't enjoy any diplomatic relations with many world-leading economies.</li>
<li> US sanctions on  nations having bilateral trade relations with Iran.</li></ul>
But even these factors don't look quite adequate to Iran from becoming the next prey to the China's debt trap diplomacy. Let's look at it more briefly.</p><br>

<h2><u>China's Debt Trap Policy</u></h2>
<p>Ever since the<b> Chinese Belt and Road Initiative</b> came into existence, China has been deliberately following<b> Modern day Colonialism </b> and Iran's slowly falling into it.<br>
Under its hook, China tends to loan excessive amounts of credit to lure various underdeveloped nations in Sub-Saharan Africa and Southern Asia.<br>
While these nations can't afford such expenditures, the huge amounts tend to corrupt their calculation. Then comes the game of Chinese debt traps into action. While most of the terms regarding the loan are not put into public domain, neither by China nor its prey and at the same time China also makes sure  that only the Chinese companies are the ones benefiting from these projects. Or the other way around,<b> there isn't any meaning of development for the host Country</b> but to squash them under piles of debt  that in many cases, the loan  is even larger than <b>50% of the national GDP.</b><br>
