        <h2>Who are Uighurs?</h2>
<img class="d-block img-fluid ml-auto mr-auto" src="../post/ui2.jpg" alt="Hong Kong Protests " style="border-radius:4px;margin-bottom:40px;">       

The Uighurs are<b> mostly Muslims</b> and number about <b>11 million</b> in western China's Xinjiang region.<br>
They see themselves as culturally and <b>ethnically close to Central Asian nations,</b> and their language is similar to<b> Turkish</b>.<br>
But in recent decades, there's been a mass migration of <b>Han Chinese </b>(China's ethnic majority) to Xinjiang, and the Uighurs feel their culture and livelihoods are under threat.<br><br>

<h2>Where is Xinjiang?</h2>
<img class="d-block img-fluid ml-auto mr-auto" src="../post/ui1.jpg" alt="Hong Kong Protests " style="border-radius:4px;margin-bottom:40px;">       


It's in the far<b> west of China </b>and is the country's biggest region. It's bordered by several countries, including India, Afghanistan and Mongolia. Like Tibet, it's an<b> autonomous region</b>, meaning it - in theory - has a degree of self-governance away from Beijing. But in practice, both face major restrictions by the central government.<br>

For centuries, the economy of Xinjiang has centred on<b> agriculture and trade</b>, and towns thrived because they were on the Silk Road.<br>
Back in the early 20th Century, the Uighurs briefly declared independence, but the region was brought under the complete control of mainland China's new Communist government in 1949.<br><br>

<h2>What's happening to people in Xinjiang?</h2>
In August 2018, a UN human rights committee was told there were credible reports that China had "<b>turned the Uighur autonomous region into something that resembles a massive internment camp</b>". About a<b> million people may have been detained</b>, the committee was told.<br>
The reports are backed by rights groups, with Human Rights Watch saying people with relatives in 26 so-called "sensitive" countries like Indonesia, Kazakhstan and Turkey have been rounded up.<br>
Anyone who has contacted someone abroad via WhatsApp is also targeted, according to HRW.<br>
Rights groups also say people in camps are made to learn Mandarin Chinese, swear loyalty to President Xi Jinping, and criticise or renounce their faith.<br>
HRW says Uighur people, in particular, are subject to intense surveillance - from facial recognition cameras to QR codes on people's doors, so officials can check the codes to see who is inside at any point. People are also reportedly made to undergo biometric tests.<br>
China says it's dealing with a threat from separatist Islamist groups, and while some Uighur Muslims have joined the Islamic State militant group, rights groups say violence in Xinjiang stems from China's oppression of people there.<br><br>

<h2>What about Uighur violence?</h2>
In 2009, riots in the regional capital Urumqi killed at least 200 people, mostly Han Chinese. Since then, there have been several attacks, including one on a police station and government offices in July 2014 that <b>killed at least 96 people</b>.<br>
Attacks blamed on Xinjiang separatists have also taken place outside the region - in October 2013, a car was driven into a crowd in Beijing's Tiananmen Square.<br>
The latest government crackdown began after <b>five people were killed</b> in a knife attack in Xinjiang in February 2017. At the time, Xinjiang's Communist Party boss Chen Quanguo urged government forces to "bury the corpses of terrorists in the vast sea of a people's war".<br><br>

<h2>What does China say?</h2>
China has said it is responding to "ethnic separatism and violent terrorist criminal activities".<br>
At a UN meeting in Geneva in August 2018, Chinese official Hu Lianhe said <b>reports of a million Uighurs being held in re-education centres were "completely untrue"</b>.<br>
But in October 2018, the top official in Xinjiang said "<b>vocational education</b>" centres had been set up and were<b> proving effective in staving off terrorism</b>.<br>
Shohrat Zakir said <b>classes were given on Chinese history, language and culture</b>, and while he didn't say whether people <b>were forced to attend</b>, he added people in the centres were provided with "nutritious, free diets".<br>
It's very unusual for China to give public explanations about how it deals with the situation in Xinjiang. And because it controls access to Xinjiang, it's been hard for anyone to receive impartial information about what is happening there.<br><br>

<h2>What is the world doing?</h2>
There's <b>growing international criticism</b> of China's treatment of Uighur Muslims but, as of yet, no country has taken any action beyond issuing critical statements.<br>
Ahead of British Prime Minister Theresa May's visit to China in January and February 2018, the UK government said it continued to be concerned over the treatment of Muslims in Xinjiang.<br>
In the US, a congressional committee on China has urged the Trump administration to place sanctions on officials and companies involved in the "ongoing human rights crisis" in Xinjiang.<br>
The committee wrote: "<b>Muslim ethnic minorities are being subjected to arbitrary detention, torture, egregious restrictions on religious practice and culture, and a digitized surveillance system so pervasive that every aspect of daily life is monitored.</b>”<br>
The new UN human rights chief Michelle Bachelet has also demanded that monitors be allowed access to Xinjiang, a request that drew an angry response from Beijing.<br><br>

<b>To conclude, we have had a lot of instances in the past, where a country has tried to suppress a targeted community, the most infamous being ‘The Holocaust.’ We don’t want our future history textbooks to show ‘The Holocaust’ and ‘tyranny of the Chinese government on Uighur Muslims’ in the same light.   <br>                                                                                             It’s our duty as a global citizen to spread awareness about it. We need to step up and pressurize the Chinese government to stop this inhumane treatment to a minority that deserves a ‘human’ life, just like every other human on this planet. </b><br><br>

